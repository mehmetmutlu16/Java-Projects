public class resolution {

    private int boy;
    private int en;

    public resolution(int boy, int en){
        this.boy = boy;
        this.en = en;
    }

    public int getBoy(){
        return boy;
    }

    public void setBoy(int boy){
        this.boy = boy;
    }

    public int getEn(){
        return en;
    }

    public void setEn(int en){
        this.en = en;
    }


}
